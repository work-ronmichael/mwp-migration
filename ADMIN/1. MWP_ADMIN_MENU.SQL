--- MAIN MENU
INSERT
INTO MWP_ADMIN_MENU
  (
    ADMIN_MENU_PARENT_ID,
    ADMIN_MENU_NAME,
    ADMIN_MENU_PATH,
    AMDIN_MENU_ISPUBLISHED,
    ADMIN_MENU_ISDELETED,
    ADMIN_MENU_TIMESTAMP,
    TEMP_ID
  )

SELECT 
  0,
  MAIN_MENU_LABEL,
  MAIN_MENU_PATH,
  1,
  0,
  SYSDATE,
  MAIN_MENU_ID
FROM PORTAL.TBL_MAIN_MENU ;


---SUB MENU
begin
  for o in (SELECT ADMIN_MENU_ID, TEMP_ID FROM MWP_ADMIN_MENU)
  loop
    EXECUTE IMMEDIATE '
    INSERT
    INTO MWP_ADMIN_MENU
      (
        ADMIN_MENU_PARENT_ID,
        ADMIN_MENU_NAME,
        ADMIN_MENU_PATH,
        AMDIN_MENU_ISPUBLISHED,
        ADMIN_MENU_ISDELETED,
        ADMIN_MENU_TIMESTAMP,
        TEMP_ID
      )
    SELECT   
      '|| o.ADMIN_MENU_ID ||',
      SUB_MENU_LABEL,
      SUB_MENU_PATH,
      1,
      0,
      SYSDATE,
      SUB_MENU_ID
    FROM PORTAL.TBL_SUB_MENU WHERE MAIN_MENU_ID = '|| o.TEMP_ID;
  end loop;
end;