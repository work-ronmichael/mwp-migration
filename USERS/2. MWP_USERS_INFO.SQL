INSERT
INTO MWP_USERS_INFO(
    USER_INFO_USER_ID,
    USER_INFO_FNAME,
    USER_INFO_LNAME,
    USER_INFO_EMAIL,
    USER_INFO_ALT_EMAIL,
    USER_INFO_ADDRESS,
    USER_INFO_BIRTHDATE,
    USER_GENDER,
    USER_INFO_TITLE,
    USER_INFO_OCCUPATION,
    USER_INFO_MOBILE
)

SELECT 
  mwp.USERID,
  prt.USER_FNAME,
  prt.USER_LNAME,
  prt.USER_EMAIL,
  prt.USER_ALT_EMAIL,
  prt.USER_ADDRESS,
  prt.USER_BIRTHDATE,
  prt.USER_GENDER,
  prt.USER_TITLE,
  prt.USER_OCCUPATION,
  prt.USER_MOBILE
FROM USERS mwp
LEFT JOIN PORTAL.TBL_USERS prt on mwp.USERNAME = prt.USER_NAME;

