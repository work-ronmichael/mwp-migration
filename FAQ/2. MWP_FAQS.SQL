INSERT
INTO MWP_FAQS
(
  FAQ_CATEGORY_ID,
  FAQ_QUESTION,
  FAQ_LINK,
  FAQ_ISPUBLISHED,
  FAQ_ISDELETED,
  FAQ_TIMESTAMP,
  TEMP_ID
)
SELECT 
  mwp.FAQ_CATEGORY_ID,
  FAQ_QUESTION,
  FAQ_LINK,
  1,
  0,
  SYSDATE,
  FAQ_ID
FROM PORTAL.TBL_FAQS por
LEFT JOIN MWP_FAQ_CATEGORY mwp  ON por.FAQ_CATEGORY_ID = mwp.FAQ_CATEGORY_ID WHERE  mwp.FAQ_CATEGORY_ID is not null;




begin
  for o in (SELECT  FAQ_CATEGORY_ID, FAQ_DESCRIPTION FROM PORTAL.TBL_FAQ_CATEGORY)
  loop
    UPDATE MWP_FAQ_CATEGORY
    SET FAQ_DESCRIPTION = o.FAQ_DESCRIPTION
    WHERE TEMP_ID = o.FAQ_CATEGORY_ID;
  end loop;
end;
