INSERT
INTO MWP_FAQ_CATEGORY
  (
    FAQ_CATEGORY_NAME,
    FAQ_TIMESTAMP,
    FAQ_ISPUBLISHED,
    FAQ_ISDELETED,
    TEMP_ID
  )
SELECT 
  FAQ_CATEGORY_NAME,
  SYSDATE,
  1,
  0,
  FAQ_CATEGORY_ID
FROM PORTAL.TBL_FAQ_CATEGORY ;


begin
  for o in (SELECT  FAQ_CATEGORY_ID, FAQ_DESCRIPTION FROM PORTAL.TBL_FAQ_CATEGORY)
  loop
    UPDATE MWP_FAQ_CATEGORY
    SET FAQ_DESCRIPTION = o.FAQ_DESCRIPTION
    WHERE TEMP_ID = o.FAQ_CATEGORY_ID;
  end loop;
end;
